/*

21. Reverse Pattern of Numbers
Description: Write a program to print numbers in reverse order.
Input: n = 5


Output:
 5 4 3 2 1


*/


for(let i=1; i<=1; i++)
{
	var s="";
	for(let j=5; j>=1; j--)
	{
		if(j>=i)
			 s+=" "+j;
	}
	console.log(s);
}



