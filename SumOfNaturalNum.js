/*



14. Sum of Natural Numbers
Description: Write a program to find the sum of the first n natural numbers.
Input: n = 5


Output: 15

*/

let num=5;
let  sum=0;

for(let i=1; i<=num; i++)
{
	sum=sum+i;
}

console.log(sum);




